def get_templates_for_domain(
    domain: Domain, max_difficulty: int = 4
) -> list[ScenarioTemplate]:
    """Get templates for a domain, optionally filtered by max difficulty."""
    return [t for t in DOMAIN_TEMPLATES[domain] if t.difficulty <= max_difficulty]


def get_all_templates() -> list[ScenarioTemplate]:
    """Get flat list of all templates across all domains."""
    return [t for templates in DOMAIN_TEMPLATES.values() for t in templates]

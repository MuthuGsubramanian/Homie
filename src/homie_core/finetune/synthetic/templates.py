"""Scenario templates for synthetic training data generation.

Contains 300 templates across 6 domains (50 per domain) that drive
the synthetic data pipeline for recursive finetuning.
"""

from __future__ import annotations

from dataclasses import dataclass
from enum import Enum


class Domain(str, Enum):
    INTENT = "intent_understanding"
    CONTEXT = "context_reasoning"
    CONVERSATIONAL = "conversational_intelligence"
    ORCHESTRATION = "task_orchestration"
    SELF_AWARENESS = "system_self_awareness"
    SAFETY = "safety_privacy"


@dataclass
class ScenarioTemplate:
    domain: Domain
    name: str
    system_context: str
    user_prompt_template: str
    good_behavior: str
    bad_behavior: str
    difficulty: int
    proactive: bool = False


